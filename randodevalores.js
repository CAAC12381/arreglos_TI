//  Rango de valores en JavaScript
console.log(" Rango de valores en JavaScript\n");

//  Boolean (true o false)
console.log(" Boolean:");
console.log("   Valores posibles: true / false\n");

//  Number (IEEE 754 - 64 bits)
console.log(" Number:");
console.log(`   Valor mínimo: ${Number.MIN_VALUE}`);
console.log(`   Valor máximo: ${Number.MAX_VALUE}`);
console.log(`   Número entero seguro mínimo: ${Number.MIN_SAFE_INTEGER}`);
console.log(`   Número entero seguro máximo: ${Number.MAX_SAFE_INTEGER}\n`);

//  BigInt (enteros muy grandes)
console.log(" BigInt:");
console.log("   No tiene un límite teórico, depende de la memoria disponible\n");

//  String (cualquier cantidad de caracteres, limitado por la memoria)
console.log(" String:");
console.log("   No tiene un límite exacto, depende de la memoria disponible\n");

//  Symbol (valores únicos, no tiene un rango numérico)
console.log(" Symbol:");
console.log("   No tiene un rango de valores, ya que cada Symbol es único\n");

//  Undefined (una variable sin valor asignado)
console.log(" Undefined:");
console.log("   Solo tiene el valor undefined\n");

//  Null (representa ausencia de valor)
console.log(" Null:");
console.log("   Solo tiene el valor null\n");

//  Object (estructura de datos dinámica)
console.log(" Object:");
console.log("   Puede contener cualquier tipo de dato, sin un límite exacto\n");

//  Array (arreglo de valores)
console.log(" Array:");
console.log("   Puede almacenar cualquier cantidad de elementos, limitado por la memoria\n");

//  Function (bloques de código ejecutable)
console.log(" Function:");
console.log("   No tiene un tamaño fijo, depende del código contenido\n");
