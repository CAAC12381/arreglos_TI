//  Ejemplo de Variables en JavaScript

// Declaración de variables con let
let nombre = "Carlos";
let edad = 18;
let activo = true;

// Declaración de constantes con const
const PI = 3.1416;

console.log("Nombre:", nombre);
console.log("Edad:", edad);
console.log("¿Está activo?", activo);
console.log("Valor de PI:", PI);

// Cambiando el valor de una variable (let permite reasignación)
nombre = "Ana";
console.log("Nuevo nombre:", nombre);

// PI = 3.14; //  Esto daría error porque const no permite reasignación
